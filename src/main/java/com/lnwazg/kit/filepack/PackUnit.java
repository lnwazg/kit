package com.lnwazg.kit.filepack;

public class PackUnit
{
    //file name size
    int n;
    
    //data size
    int d;
    
    public int getN()
    {
        return n;
    }
    
    public PackUnit setN(int n)
    {
        this.n = n;
        return this;
    }
    
    public int getD()
    {
        return d;
    }
    
    public PackUnit setD(int d)
    {
        this.d = d;
        return this;
    }
    
}
